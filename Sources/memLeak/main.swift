func createKey(from keyData: Data, publicKey: Bool) {
    
    // Create a memory BIO...
    let bio = BIO_new(BIO_s_mem())
    
    defer {
        BIO_free(bio)
    }
    
    // Create a BIO object with the key data...
    keyData.withUnsafeBytes() { (buffer: UnsafePointer<UInt8>) in
        BIO_write(bio, buffer, Int32(keyData.count))
        // The below is equivalent of BIO_flush...
        BIO_ctrl(bio, BIO_CTRL_FLUSH, 0, nil)
    }
    
    var evp_key = EVP_PKEY_new()
    
    // Read in the key data and process depending on key type...
    if publicKey {
        PEM_read_bio_PUBKEY(bio, &evp_key, nil, nil)
    } else {
        PEM_read_bio_PrivateKey(bio, &evp_key, nil, nil)
    }
    EVP_PKEY_free(evp_key)
}

createKey(from: rsaPubKey.data(using: .utf8)!, publicKey: true)
createKey(from: rsaPrivKey.data(using: .utf8)!, publicKey: false)
